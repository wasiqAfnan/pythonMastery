'''
Operator associativity and precedence are important concepts in Python.

Operator associativity refers to the order in which operators are evaluated.
For example, in the expression a + b * c, the multiplication operator is evaluated before the addition operator.

Operator precedence refers to the order in which operators are evaluated.
For example, in the expression a + b * c, the multiplication operator has a higher precedence than the addition operator.

In Python, the operators are evaluated from left to right.
'''

'''
precedence:

1. Parentheses: ()
2. Exponentiation: **
3. Multiplication: *, /, //, % 
4. Addition: +, -
5. Bitwise shift: <<, >>
6. Bitwise and: &
7. Bitwise xor: ^
8. Bitwise or: |
9. Comparisons: <, >, <=, >=, ==, !=, is, is not
10. Logical and: and
11. Logical or: or
'''


'''
Operator associativity:

+,-,*,/,//,%    left to right
comparison(== >=, <=)       left to right
**              right to left
=, += -=        right to left
'''
