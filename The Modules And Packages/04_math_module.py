# math module has many fucntions like sin, cos, sqrt, pow, etc.

import math
print(math.sin(math.pi/2)) # prints the sine of pi/2
print(math.cos(math.pi)) # prints the cosine of pi
print(math.sqrt(25)) # prints the square root of 25
print(math.pow(2, 3)) # prints 8
print(math.ceil(3.7)) # prints 4
print(math.floor(3.7)) # prints 3
print(math.log(1000)) # prints 6.907755278982137
print(math.log10(1000)) # prints 3
print(math.pi) # prints 3.141592653589793